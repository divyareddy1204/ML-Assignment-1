_area_of_circle_ = 3.14 * (radius ** 2)  # calculating the area of the circle
print("Area of the circle is", _area_of_circle_)  # printing the area
_circum_of_circle_ = 2 * 3.14 * radius  # calculating circumference of the circle
print("Circumference of the circle is", _circum_of_circle_)  # printing the circumference
